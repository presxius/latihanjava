package com.company.belajarjava;


public class HierarkiOperator {
    
    public static void main(String[] args) {
        int A, B;
        
        A = 8 + 2 * 3 / 6;
        B = (8 + 2) * 3 / 6;
        System.out.println("Hasil dari A = " + A);
        System.out.println("Hasil dari B = " + B);
    }
    
}